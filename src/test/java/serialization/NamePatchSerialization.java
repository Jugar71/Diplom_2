package serialization;

public class NamePatchSerialization {
    private String name;

    public NamePatchSerialization() {}

    public NamePatchSerialization(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
